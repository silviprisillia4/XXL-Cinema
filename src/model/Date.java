package model;

public class Date {
    private String date;
    private int dateSubstring;
    private int monthSubstring;
    private int yearSubstring;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getDateSubstring() {
        return dateSubstring;
    }

    public void setDateSubstring(int dateSubstring) {
        this.dateSubstring = dateSubstring;
    }

    public int getMonthSubstring() {
        return monthSubstring;
    }

    public void setMonthSubstring(int monthSubstring) {
        this.monthSubstring = monthSubstring;
    }

    public int getYearSubstring() {
        return yearSubstring;
    }

    public void setYearSubstring(int yearSubstring) {
        this.yearSubstring = yearSubstring;
    }
    
}
